# Подключаем библиотеку DrWatson для организации проекта:
# она помогает управлять зависимостями, путями к файлам и результатами моделирования.
using DrWatson
 
# Активируем виртуальное окружение Julia для проекта с названием "project".
# Это гарантирует использование корректных версий пакетов, указанных в проекте.
@quickactivate "project"
 
# Загружаем пакет Agents.jl — он нужен для создания агент‑ориентированных моделей,
# где отдельные «агенты» взаимодействуют между собой и с окружением.
using Agents
 
# Подключаем пакет DataFrames для работы с табличными данными.
# В нашем случае он будет использоваться для сбора и анализа статистики по агентам.
using DataFrames
 
# Импортируем универсальную библиотеку Plots для визуализации данных.
using Plots
 
# Подключаем бэкенд CairoMakie для построения графиков.
# Он обеспечивает высокое качество визуализации и гибкость настройки.
using CairoMakie
 
# Встраиваем код из файла daisyworld.jl (расположенного в директории src)
# в текущий скрипт. В этом файле содержится определение модели Daisyworld:
# правила поведения агентов‑«маргариток», динамика температуры и т. д.
include(srcdir("daisyworld.jl"))
 
# Определяем функцию black: проверяет, относится ли агент к «чёрным маргариткам».
# Возвращает true, если порода агента (breed) равна :black.
black(a) = a.breed == :black
 
# Определяем функцию white: проверяет, относится ли агент к «белым маргариткам».
# Возвращает true, если порода агента (breed) равна :white.
white(a) = a.breed == :white
 
# Создаём список агрегированных данных (adata) для сбора статистики по агентам во время симуляции.
# Каждая пара содержит:
# - функцию‑предикат (black или white) для отбора агентов определённого типа;
# - функцию count для подсчёта количества отобранных агентов.
# В результате будут собираться данные о количестве чёрных и белых маргариток на каждом шаге.
adata = [(black, count), (white, count)]
 
## Определяем параметры эксперимента в виде словаря (Dict)
param_dict = Dict(
    # Размеры сетки модели (30×30 клеток).
    :griddims => (30, 30),
    # Возможные значения максимального возраста маргариток: 25 или 40.
    # Это создаст комбинации для исследования влияния долголетия агентов.
    :max_age => [25, 40],
    # Доля начальных белых маргариток: от 0.2 до 0.8 (20 %–80 %).
    :init_white => [0.2, 0.8],
    # Начальная доля чёрных маргариток — фиксированная (20 %).
    :init_black => 0.2,
    # Альбедо (отражательная способность) белых маргариток (0.75 — высокое).
    :albedo_white => 0.75,
    # Альбедо чёрных маргариток (0.25 — низкое).
    :albedo_black => 0.25,
    # Альбедо поверхности планеты без маргариток.
    :surface_albedo => 0.4,
    # Скорость изменения солнечной светимости (0.005 за шаг).
    :solar_change => 0.005,
    # Начальная солнечная светимость (базовое значение).
    :solar_luminosity => 1.0,
    # Сценарий моделирования по умолчанию.
    :scenario => :default,
    # Зерно генератора случайных чисел (для воспроизводимости результатов).
    :seed => 165,
)
 
## Создаём список всех возможных комбинаций параметров
# Функция dict_list генерирует все комбинации значений параметров,
# особенно для тех, что заданы списками (max_age, init_white).
# Например, будут созданы комбинации:
# - max_age=25, init_white=0.2;
# - max_age=25, init_white=0.8;
# - max_age=40, init_white=0.2;
# - max_age=40, init_white=0.8.
params_list = dict_list(param_dict)
 
# Запускаем цикл по всем комбинациям параметров
for params in params_list
    # Создаём экземпляр модели Daisyworld с текущими параметрами
    # Оператор ;params... распаковывает словарь параметров в аргументы функции.
    model = daisyworld(;params...)
 
    # Запускаем симуляцию на 1000 временных шагов (ticks).
    # Функция run! выполняет моделирование и собирает данные согласно настройкам adata.
    # Результаты сохраняются в двух DataFrame:
    # - agent_df: статистика по агентам (количество чёрных и белых маргариток по времени);
    # - model_df: глобальные параметры модели (температура, солнечная светимость и т. д.).
    agent_df, model_df = run!(model, 1000; adata)
 
    # Создаём фигуру (графическое окно) размером 600×400 пикселей.
    figure = Figure(size = (600, 400))
 
    # Добавляем на фигуру оси координат в позицию [1, 1] (первая строка, первый столбец).
    # Настраиваем подписи осей:
    # - xlabel = "tick" — ось X отображает номер временного шага симуляции;
    # - ylabel = "daisy count" — ось Y отображает количество маргариток.
    ax = figure[1, 1] = Axis(figure, xlabel = "tick", ylabel = "daisy count")
 
    # Рисуем линию для чёрных маргариток:
    # - используем данные из agent_df: время (столбец :time) и количество (:count_black);
    # - цвет линии — чёрный (:black).
    blackl = lines!(ax, agent_df[!, :time], agent_df[!, :count_black], color = :black)
 
    # Рисуем линию для белых маргариток:
    # - данные: время и количество белых маргариток (:count_white);
    # - цвет линии — оранжевый (:orange), чтобы чётко отличать от чёрных.
    whitel = lines!(ax, agent_df[!, :time], agent_df[!, :count_white], color = :orange)
 
    # Добавляем легенду в позицию [1, 2] (первая строка, второй столбец):
    # - связываем линии blackl и whitel с подписями "black" и "white";
    # - задаём размер шрифта в легенде (labelsize = 12).
    Legend(figure[1, 2], [blackl, whitel], ["black", "white"], labelsize = 12)
 
    # Формируем имя файла для сохранения графика с учётом текущих параметров.
    # Функция savename создаёт строку с параметрами в удобочитаемом формате.
    plt_name = savename("daisy-count", params) * ".png"
 
    # Сохраняем итоговый график в файл. Путь к папке для сохранения определяется автоматически
    # через plotsdir в рамках структуры проекта DrWatson.
    save(plotsdir(plt_name), figure)
end
