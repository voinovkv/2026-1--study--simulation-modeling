# # Скрипт визуализации результатов сканирования β 
# **Цель:** Визуализировать результаты сканирования параметра β (коэффициента заразности) в модели SIR с миграцией.
# Построить составной график с тремя подграфиками для анализа:
# * зависимости пика эпидемии и конечной доли инфицированных от β;
# * числа умерших от β;
# * доли выздоровевших от β.



using DrWatson
@quickactivate "project"
using Agents, DataFrames, Plots, CSV

# Загружаем реализацию базовой модели 
include(srcdir("sir_model.jl"))

# Загружаем результаты сканирования
df = CSV.read(datadir("beta_scan_all.csv"), DataFrame)

# Создаём составной график
# Подграфик 1: Пик и конечная доля инфицированных
# Отображаем две линии:
# * «Пик» — максимальная доля инфицированных за время симуляции;
# * «Конечная» — доля инфицированных на последнем шаге симуляции.

p1 = plot(df.beta, df.peak, label = "Пик", xlabel = "β", ylabel = "Доля инфицированных")
plot!(p1, df.beta, df.final_inf, label = "Конечная")

# Подграфик 2: Число умерших
# Отображаем зависимость общего числа смертей от коэффициента заразности β.

p2 = plot(df.beta, df.deaths, xlabel = "β", ylabel = "Число умерших")

# Подграфик 3: Доля выздоровевших
# Отображаем долю выздоровевших/иммунных агентов от начального населения.

p3 = plot(df.beta, df.final_rec, xlabel = "β", ylabel = "Доля выздоровевших")

# Объединяем три подграфика в один составной график

plot(p1, p2, p3, layout = (3, 1), size = (800, 900))

# Сохраняем составной график в папку plots

savefig(plotsdir("comprehensive_analysis.png"))