# # Анализ динамики SIR‑модели с миграцией между городами
# **Цель:** Исследовать распространение инфекции в системе из трёх городов с учётом миграции населения, разной заразности на стадиях болезни и возможности повторного заражения. Проанализировать динамику популяций S/I/R и влияние смертности.

# ## Инициализация проекта и загрузка пакетов
# Подключаем необходимые пакеты для работы с агент‑ориентированным моделированием, анализом данных, визуализацией и сохранением результатов.
using DrWatson
@quickactivate "project"

using Agents, DataFrames, Plots
using JLD2

# Загружаем реализацию модели из отдельного файла `sir_model.jl`.
include(srcdir("sir_model.jl"))

# ## Параметры эксперимента
# Задаём параметры модели в виде словаря для удобства модификации и воспроизводимости результатов.
# Каждый параметр имеет следующее значение:
# * `:Ns` — численность населения в каждом из трёх городов (изначально по 1000 человек);
# * `:β_und` — вероятность передачи инфекции невыявленными носителями (высокая заразность, 0.5);
# * `:β_det` — вероятность передачи выявленными носителями (после обнаружения, низкая заразность, 0.05);
# * `:infection_period` — длительность инфекционного периода в днях (14 дней, после чего наступает выздоровление или смерть);
# * `:detection_time` — время до выявления инфекции (7 дней);
# * `:death_rate` — вероятность смерти при выздоровлении (2 %, 0.02);
# * `:reinfection_probability` — вероятность повторного заражения для иммунных (10 %, 0.1);
# * `:Is` — начальное количество инфицированных в каждом городе (заражение начинается в третьем городе: [0, 0, 1]);
# * `:seed` — зерно генератора случайных чисел для воспроизводимости результатов (42);
# * `:n_steps` — количество шагов моделирования (дней, 100).
params = Dict(
    :Ns => [1000, 1000, 1000],
    :β_und => [0.5, 0.5, 0.5],
    :β_det => [0.05, 0.05, 0.05],
    :infection_period => 14,
    :detection_time => 7,
    :death_rate => 0.02,
    :reinfection_probability => 0.1,
    :Is => [0, 0, 1],
    :seed => 42,
    :n_steps => 100,
)

# ## Инициализация модели
# Создаём экземпляр модели с заданными параметрами, используя функцию `initialize_sir()`
model = initialize_sir(; params...)

# ## Подготовка массивов для хранения данных
# Создаём пустые массивы для фиксации численности популяций на каждом шаге симуляции.
times = Int[]
S_vals = Int[]
I_vals = Int[]
R_vals = Int[]
total_vals = Int[]

# ## Запуск симуляции вручную
# Проводим моделирование по дням (шагам), выполняя один шаг модели с помощью `Agents.step!(model, 1)`.
# На каждом шаге:
# 1. Обновляем состояние всех агентов (миграция, передача инфекции, выздоровление/смерть).
# 2. Фиксируем численность популяций с помощью вспомогательных функций:
for step = 1:params[:n_steps]
    Agents.step!(model, 1)

    push!(times, step)
    push!(S_vals, susceptible_count(model))
    push!(I_vals, infected_count(model))
    push!(R_vals, recovered_count(model))
    push!(total_vals, total_count(model))
end

# ## Создание DataFrame для удобства анализа
# Преобразуем массивы данных в табличный формат с помощью пакета `DataFrames`:
# * `agent_df` содержит данные по популяциям S, I, R и времени;
# * `model_df` содержит общее количество агентов во времени.
agent_df = DataFrame(time = times, susceptible = S_vals, infected = I_vals, recovered = R_vals)
model_df = DataFrame(time = times, total = total_vals)

# ## Визуализация динамики популяций
# Строим график изменения численности популяций во времени с помощью пакета `Plots`.
plot(
    agent_df.time,
    agent_df.susceptible,
    label = "Восприимчивые",
    xlabel = "Дни",
    ylabel = "Количество",
)
plot!(agent_df.time, agent_df.infected, label = "Инфицированные")
plot!(agent_df.time, agent_df.recovered, label = "Выздоровевшие")
plot!(agent_df.time, model_df.total, label = "Всего (включая умерших)", linestyle = :dash)
savefig(plotsdir("sir_basic_dynamics.png"))

# ## Сохранение данных для дальнейшего анализа
# Сохраняем результаты симуляции в формате JLD2 с помощью макроса `@save`:
# * `sir_basic_agent.jld2` — данные по популяциям (DataFrame `agent_df`);
# * `sir_basic_model.jld2` — общее количество агентов во времени (DataFrame `model_df`).
@save datadir("sir_basic_agent.jld2") agent_df
@save datadir("sir_basic_model.jld2") model_df